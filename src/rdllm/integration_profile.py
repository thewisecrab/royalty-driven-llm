def validate_integration_profile_shape(profile: dict[str, Any]) -> list[str]:
    errors: list[str] = []
    required = (
        "profile_version",
        "issuer",
        "created_at",
        "provider",
        "api_contract",
        "verifier_contract",
        "schemas",
        "public_surfaces",
        "certification",
        "bound_artifacts",
        "sample_response",
        "readiness_checks",
        "summary",
        "privacy",
        "profile_hash",
        "signature",
    )
    for key in required:
        if key not in profile:
            errors.append(f"missing integration profile field: {key}")
    if errors:
        return errors
    if profile.get("profile_version") != INTEGRATION_PROFILE_VERSION:
        errors.append("integration profile version is unsupported")
    for surface in REPLAY_REQUIRED_PUBLIC_SURFACES:
        if surface not in profile.get("public_surfaces", {}):
            errors.append(f"missing integration public surface: {surface}")
    for schema_name in SCHEMA_MAP:
        if schema_name in FORWARD_COMPAT_SCHEMAS:
            continue
        if schema_name not in profile.get("schemas", {}):
            errors.append(f"missing integration schema: {schema_name}")
    return errors


def _normalize_expected_profile_for_replay(
    expected: dict[str, Any], profile: dict[str, Any]
) -> dict[str, Any]:
    """Keep old signed profiles replayable when newer optional surfaces exist."""

    normalized = dict(expected)

    public_surfaces = dict(expected.get("public_surfaces", {}))
    profile_surfaces = profile.get("public_surfaces", {})
    for surface in FORWARD_COMPAT_PUBLIC_SURFACES:
        if surface not in profile_surfaces:
            public_surfaces.pop(surface, None)
    normalized["public_surfaces"] = public_surfaces

    schemas = dict(expected.get("schemas", {}))
    profile_schemas = profile.get("schemas", {})
    for schema in FORWARD_COMPAT_SCHEMAS:
        if schema not in profile_schemas:
            schemas.pop(schema, None)
    normalized["schemas"] = schemas

    api_contract = dict(expected.get("api_contract", {}))
    profile_endpoint_names = {
        endpoint.get("name")
        for endpoint in profile.get("api_contract", {}).get("endpoints", [])
        if isinstance(endpoint, dict)
    }
    api_contract["endpoints"] = [
        endpoint
        for endpoint in api_contract.get("endpoints", [])
        if endpoint.get("name") not in FORWARD_COMPAT_ENDPOINTS
        or endpoint.get("name") in profile_endpoint_names
    ]
    normalized["api_contract"] = api_contract

    verifier_contract = dict(expected.get("verifier_contract", {}))
    profile_commands = set(
        profile.get("verifier_contract", {}).get("reference_cli_commands", [])
    )
    verifier_contract["reference_cli_commands"] = [
        command
        for command in verifier_contract.get("reference_cli_commands", [])
        if command not in FORWARD_COMPAT_VERIFIER_COMMANDS
        or command in profile_commands
    ]
    profile_failure_modes = set(
        profile.get("verifier_contract", {}).get("required_failure_modes", [])
    )
    verifier_contract["required_failure_modes"] = [
        mode
        for mode in verifier_contract.get("required_failure_modes", [])
        if mode not in FORWARD_COMPAT_FAILURE_MODES or mode in profile_failure_modes
    ]
    normalized["verifier_contract"] = verifier_contract

    summary = dict(expected.get("summary", {}))
    summary["schema_count"] = len(schemas)
    summary["public_surface_count"] = len(public_surfaces)
    summary["required_public_surface_count"] = len(public_surfaces)
    summary["endpoint_count"] = len(api_contract.get("endpoints", []))
    summary["forward_compatible_public_surface_count"] = sum(
        1 for surface in FORWARD_COMPAT_PUBLIC_SURFACES if surface in profile_surfaces
    )
    profile_summary = profile.get("summary", {})
    for optional_summary_field in (
        "provider_public_surface_count",
        "required_public_surface_count",
        "replay_required_public_surface_count",
        "forward_compatible_public_surface_count",
    ):
        if optional_summary_field not in profile_summary:
            summary.pop(optional_summary_field, None)
    normalized["summary"] = summary
    normalized["profile_hash"] = hash_payload(_hashable_profile(normalized))
    return normalized


def verify_integration_profile(
    profile: dict[str, Any],
    *,
    provider_card: dict[str, Any],
    certification_report: dict[str, Any],
    response_envelope: dict[str, Any],
    assurance_bundle: dict[str, Any] | None = None,
    certification_attestation: dict[str, Any] | None = None,
    signing_secret: str | None = None,
) -> list[str]:
    """Verify an integration profile against public provider proof artifacts."""

    errors = validate_integration_profile_shape(profile)
    if errors:
        return errors

    expected_hash = hash_payload(_hashable_profile(profile))
    if expected_hash != profile.get("profile_hash"):
        errors.append("integration profile hash is not reproducible")

    errors.extend(
        f"provider card: {error}" for error in validate_provider_card_shape(provider_card)
    )
    errors.extend(
        f"response envelope: {error}"
        for error in verify_response_envelope(
            response_envelope,
            signing_secret=signing_secret,
        )
    )
    if assurance_bundle:
        errors.extend(
            f"assurance bundle: {error}"
            for error in validate_assurance_bundle_shape(assurance_bundle)
        )

    expected_certification_attestation = (
        certification_attestation
        if profile.get("bound_artifacts", {}).get("certification_attestation_hash")
        else None
    )
    expected_assurance_bundle = (
        assurance_bundle
        if profile.get("bound_artifacts", {}).get("assurance_bundle_hash")
        else None
    )
    expected = make_integration_profile(
        provider_card=provider_card,
        certification_report=certification_report,
        response_envelope=response_envelope,
        assurance_bundle=expected_assurance_bundle,
        certification_attestation=expected_certification_attestation,
        issuer=profile.get("issuer", DEFAULT_ISSUER),
        created_at=profile.get("created_at", ""),
        signing_secret=signing_secret,
    )
    expected = _normalize_expected_profile_for_replay(expected, profile)
    for key in (
        "provider",
        "api_contract",
        "verifier_contract",
        "schemas",
        "public_surfaces",
        "certification",
        "bound_artifacts",
        "sample_response",
        "readiness_checks",
        "summary",
        "privacy",
    ):
        if expected.get(key) != profile.get(key):
            errors.append(f"integration profile {key} does not match artifacts")
    if expected.get("profile_hash") != profile.get("profile_hash"):
        errors.append("integration profile hash does not match artifacts")

    readiness = profile.get("readiness_checks", {})
    if profile.get("summary", {}).get("status") != "ready":
        errors.append("integration profile status is not ready")
    for check, passed in readiness.items():
        if passed is not True:
            errors.append(f"integration readiness check failed: {check}")

    signature = profile.get("signature", {})
    if signing_secret:
        expected_signature = sign_payload(_hashable_profile(profile), signing_secret)
        if signature.get("algorithm") != "HMAC-SHA256":
            errors.append("integration profile is not HMAC signed")
        elif signature.get("value") != expected_signature:
            errors.append("integration profile signature is invalid")

    return errors
